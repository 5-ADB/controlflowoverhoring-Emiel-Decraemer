// 1. for lus
for (let variabele = 0; variabele >= 10; variabele++) {
    console.log(variabele);
    
}
// 2. if else
if (variabele >= 10) {
    console.log("hoger dan 10");
} else {
    console.log("lager dan 10");
}
// 3. do while
do {
    console.log("kleiner dan 10 ");
    variabele++
} while (variabele >= 10);